/* ============================================================
   AI 指令模板庫 — 互動視覺化（模式二）
   每個模板採 SDD 結構：目標 / 規格 / 限制 / 驗收。
   老師複製完整 prompt → 貼到 Claude / ChatGPT / Gemini →
   把生成的 HTML 貼回「新增互動視覺化」執行。
   ============================================================ */

export interface PromptTemplate {
  id: string;
  category: TemplateCategory;
  title: string;
  summary: string; // 卡片上的一句話說明
  goal: string; // 目標：教學目的
  spec: string; // 規格：畫面與互動要求
  acceptance: string; // 驗收：怎樣算成功
  extraConstraints?: string; // 模板特有限制（附加在共用限制之後）
}

export type TemplateCategory =
  | "語文遊戲"
  | "數學視覺化"
  | "自然模擬"
  | "社會人文"
  | "課堂經營"
  | "評量互動";

export const TEMPLATE_CATEGORIES: TemplateCategory[] = [
  "語文遊戲",
  "數學視覺化",
  "自然模擬",
  "社會人文",
  "課堂經營",
  "評量互動",
];

/** 所有模板共用的技術限制（SDD「限制」段基底） */
export const SHARED_CONSTRAINTS = `- 產出「單一完整 HTML 檔」：所有 CSS 與 JavaScript 皆內嵌，不得引用任何外部資源（CDN、圖片網址、字型、API）
- 程式會在隔離的 iframe 沙箱（sandbox="allow-scripts"）中執行：不要使用 localStorage、cookie、alert/confirm/prompt，狀態一律存在 JavaScript 變數
- 這是課堂投影工具：主要文字至少 28px、按鈕至少 48px 高、對比清晰、配色柔和不刺眼
- 介面語言：繁體中文（台灣用語）
- 不需要說明文件，直接輸出完整 HTML 程式碼`;

/** 組合單一模板的完整 prompt（複製用） */
export function buildPrompt(t: PromptTemplate): string {
  return `請幫我製作一個課堂互動視覺化網頁。

## 目標
${t.goal}

## 規格
${t.spec}

## 限制
${SHARED_CONSTRAINTS}${t.extraConstraints ? `\n${t.extraConstraints}` : ""}

## 驗收標準
${t.acceptance}`;
}

